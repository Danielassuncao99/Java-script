// mais sobre Stings


// formas de lidar com strings
//               0123456789
let umaString = 'um texto';
console.log(umaString.charAt(1));  // charAt seleciona a letra que vc quer para exibir

// 3 fomas de concatenação 

console.log(umaString.concat('em um lindo dia.')); // concatenação
console.log(umaString + 'em um lindo dia.'); // concatenação
console.log(`${umaString} em um lindo dia`); // concatenação forma que utiliza no mercado de trabalho.

